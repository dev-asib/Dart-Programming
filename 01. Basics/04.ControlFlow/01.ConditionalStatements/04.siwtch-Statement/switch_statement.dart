void main() {
  int month = 4;

  switch (month) {
    case 1:
      print("Sunday");
    case 2:
      print("Monday");
      break;
    case 3:
      print("Tuesday");
    case 4:
      print("Wednesday");
    case 5:
      print("Thursday");
    case 6:
      print("Friday");
    case 7:
      print("Saturday");
    default:
      print("Unknown");
  }
}
