void main() {
  // Arithmetic Operators
  // + (Addition): print(5 + 3); // 8
  // - (Subtraction): print(5 - 3); // 2
  // * (Multiplication): print(5 * 3); // 15
  // / (Division): print(5 / 2); // 2.5
  // ~/ (Integer Division): print(5 ~/ 2); // 2
  // % (Modulus): print(5 % 2); // 1

  int numb1 = 10;
  double numb2 = 20;
  int numb3 = 13;

  // Add
  print(numb1 + numb2);

  // Subtract
  print(numb2 - numb1);

  // Multiply
  print(numb1 * numb2);

  // Divide
  print(numb3 / numb1);

  // Modulo
  print(numb2 % numb1);
  print(numb3 % numb1);

  // Divide (Returning an integer Result);
  print(numb2 ~/ numb3);

  // Unary Minus
  print(-numb2);
}
