void main() {
  int numb1 = 10;
  double numb2 = 20;
  int numb3 = 13;

  // Add
  print(numb1 + numb2);

  // Subtract
  print(numb2 - numb1);

  // Multiply
  print(numb1 * numb2);

  // Divide
  print(numb3 / numb1);

  // Modulo
  print(numb2 % numb1);
  print(numb3 % numb1);

  // Divide (Returning an integer Result);
  print(numb2 ~/ numb3);

  // Unary Minus
  print(-numb2);
}
