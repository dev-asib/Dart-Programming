void main(){
  int x = 10;
  int y = 20;
  int z = 30;

  // Prefix Increment and Decrement Operators
  print(--x);
  --y;
  print(y);

  print(++z);
  ++z;
  print(z);

  int a = 50;
  int b = 60;
  int c = 70;

  // Postfix Increment and Decrement Operators
  print(a--);
  print(a);
  --a;
  print(a);

  print(b++);
  print(b);
  c++;
  print(c);

}