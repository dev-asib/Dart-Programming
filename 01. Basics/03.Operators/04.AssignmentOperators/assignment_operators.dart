void main() {
  // =	Assign	var x = 5; print(x); // 5
  // +=	Add and assign	x += 3; print(x); // 8
  // -=	Subtract and assign	x -= 2; print(x); // 6
  // *=	Multiply and assign	x *= 2; print(x); // 12
  // /=	Divide and assign	x /= 4; print(x); // 3.0
  // ~/=	Integer divide and assign	x ~/= 3; print(x); // 1
  // %=	Modulus and assign	x %= 3; print(x); // 1

  // Assign
  int x = 5;
  int y = 11;
  double z = 17.0;
  print(x);

  // Add and assign
  x += 5;
  print(x);

  // Subtract and assign
  x -= 3;
  print(x);

  // Multiply and assign
  x *= 3;
  print(x);

  // Integer Divide and assign
  x ~/= 8;
  print(x);

  // Modulus and assign
  y %= 3;
  print(y);

  // Divide and assign
  z /= 8;
  print(z);
}
