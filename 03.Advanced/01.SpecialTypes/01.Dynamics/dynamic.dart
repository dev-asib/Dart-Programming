void main() {
  dynamic data = "Dhaka";
  print(data);

  data = 120;
  print(data);

  data = 5.50;
  print(data);

  data = true;
  print(data);

  data = [1, 2, "Bangladesh", true, false, 30.5];
  print(data);

  data = {50, 5.0, 'Dhaka', false};
  print(data);

  data = {'name': 'Asib', 'id': 444422, true: 'isDeveloper'};
  print(data);

  data = null;
  print(data);
}
